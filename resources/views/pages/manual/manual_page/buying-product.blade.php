<strong>เลือกซื้อสินค้า & เพิ่มสินค้าลงในตระกร้า</strong><br>
1.เมนูด้านซ้ายคลิกที่ "สินค้าจากบริษัทและร้านค้า" เพื่อยังหน้าสินค้า<br><br>
<img src="/images/manual/buying-product/left-bar.png">

<br><br>

2.เลือกหมวดหมู่สินค้าที่ต้องการ หากต้องการดูสินค้าทั้งหมดให้คลิกที่ "แสดงสินค้าทั้งหมด" เพื่อไปยังหน้าแสดงสินค้าทั้งหมดของหมวดหมู่นั้นๆ<br><br>
<img src="/images/manual/buying-product/buying-product-1.png">

<br><br>
3.หากต้องการดูหมวดหมู่ย่อยทั้งหมดให้คลิกที่ "แสดงหมวดสินค้าที่เกี่ยวข้อง"<br><br>
<img src="/images/manual/buying-product/buying-product-2.png">

<br><br>

4.เมื่อเจอสินค้าที่ต้องการแล้วให้ตลิกที่สินค้า จากนั้นจะไปยังหน้ารายละเอียดสินค้า<br><br>
<img src="/images/manual/buying-product/buying-product-3.png">

<br><br>

5.เมื่อต้องการเพิ่มสินค้าลงในตระกร้าสินค้า ระบุจำนวนสินค้า หากสินค้ามีตัวเลือกให้เลือกตัวเลือกสินค้า จากนั้นคลิกที่ "ใส่ตระกร้า" สินค้าจะถูกเพิ่มลงในตระกร้า<br><br>
<img src="/images/manual/buying-product/buying-product-4.png">

