1.เมนูด้านบนคลิกรูปถุงช้อปปิ้ง เมื่อคลิกแล้วจะแสดงสินค้าในตระกร้า<br><br>
<img src="/images/manual/checking-out-product/checking-out-product-1.png">

<br><br>

2.คลิกที่ "ดำเนินการสั่งซื้อ" เพื่อไปยังหน้าดำเนินการสั่งซื้อ<br><br>
<img src="/images/manual/checking-out-product/checking-out-product-2.png">

<br><br>

3.ตรวจสอบรายการสินค้า จำนวนสินค้าที่สั่งซื้อ กรอกที่อยู่สำหรับจัดส่ง ข้อความถึงผู้ขาย<br><br>
<img src="/images/manual/checking-out-product/checking-out-product-3.png">

<br><br>

4.เลือกช่องทางการจัดส่งของการสั่งซื้อสินค้า<br><br>
<img src="/images/manual/checking-out-product/checking-out-product-4.png">

<br><br>

5.เมื่อตรวจสอบรายการสั่งซื้อเรียบร้อยแล้ว (ในกรณีที่มีมากกว่า 1 ร้านค้าโปรดตรวจสอบรายการสั่งซื้อและกรอกข้อมูลให้ครบถ้วนในแต่ละร้านค้าก่อนการดำเนินการสั่งซื้อ) และต้องการดำเนินการสั่งซื้อให้คลิกที่ "สั่งซื้อสินค้า" เพื่อดำเนินการสั่งซื้อ<br><br>
<img src="/images/manual/checking-out-product/checking-out-product-5.png">

<br><br>

6.รายการสั่งซื้อจะถูกส่งไปยังผู้ขายของแต่ละร้านค้าและจะแสดงข้อความ "การสั่งซื้อสินค้าของคุณเรียบร้อยแล้ว" เป็นการบ่งบอกว่าการสั่งซื้อสินค้าเรียบร้อยแล้ว<br><br>
<img src="/images/manual/checking-out-product/checking-out-product-6.png">
