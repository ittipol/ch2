เพิ่มวิธีการชำระเงิน เพื่อใช้แสดงข้อมูลและเป็นตัวเลือกสำหรับการชำระเงินของผู้ซื้อ
<br><br>

<strong>ขั้นตอนการเพิ่มตัวเลือกวิธีการชำระเงิน</strong><br>

1.เมนูด้านบนคลิกรูปโปรไฟล์ เมื่อคลิกแล้วจะแสดงร้านค้าของคุณ<br><br>
<img src="/images/manual/adding-payment-method/top-bar.png">
<br><br>
<img src="/images/manual/adding-payment-method/my-shop.png">

<br><br>

2.คลิกที่ "เพิ่มเติม" จะแสดงเมนูเพิ่มเติม จากนั้นคลิกที่ "จัดการสินค้า" เพื่อไปยังหน้าจัดการสินค้า<br><br>
<img src="/images/manual/adding-payment-method/my-shop-overview-btn.png">

<br><br>

3.คลิกที่ "วิธีการชำระเงิน" เพื่อไปยังหน้าวิธีการชำระเงิน<br><br>
<img src="/images/manual/adding-payment-method/payment-method.png">

<br><br>

4.เลือกหัวข้อที่ต้องการเพิ่ม จากนั้นคลิกที่ "เพิ่มวิธีตามหัวข้อที่เลือก"<br><br>
<img src="/images/manual/adding-payment-method/adding-payment-method-1.png">

<br><br>

5.กรอกข้อมูลการชำระเงินคาที่ระบุไว้ตามแต่ละวิธีการที่เลือกก่อนหน้า<br><br>
<img src="/images/manual/adding-payment-method/adding-payment-method-2.png">

<br><br>

6.เมื่อกรอกข้อมูลครบถ้วนแล้วจากนั้นคลิกที่ "บันทึก" เพื่อบันทึกวิธีการชำระเงิน<br><br>
<img src="/images/manual/adding-payment-method/adding-payment-method-3.png">

<br><br>

7.ข้อมูลที่กรอกจะแสดงตามหัวข้อที่ โดยในตัวอย่างนี้ได้เลือกเพำิ่มหัวข้อ "โอนเงินผ่านธนาคาร" ดังนั้นเมื่อบันทึกข้อมูลแล้ว ข้อมูลจะถูกแสดงในหัวข้อ "โอนเงินผ่านธนาคาร"<br><br>
<img src="/images/manual/adding-payment-method/adding-payment-method-4.png">

<br><br>

8.สามารถแก้ไขหรือลบได้หากต้องการ<br><br>
