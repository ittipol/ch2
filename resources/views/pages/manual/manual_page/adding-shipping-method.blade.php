เพิ่มวิธีการจัดส่ง เพื่อใช้แสดงข้อมูลและเป็นตัวเลือกสำหรับการจัดส่งของผู้ซื้อในหน้าดำเนินการสั่งซื้อ
<br><br>

<strong>ขั้นตอนการเพิ่มตัวเลือกวิธีการจัดส่ง</strong><br>

1.เมนูด้านบนคลิกรูปโปรไฟล์ เมื่อคลิกแล้วจะแสดงร้านค้าของคุณ<br><br>
<img src="/images/manual/adding-shipping-method/top-bar.png">
<br><br>
<img src="/images/manual/adding-shipping-method/my-shop.png">

<br><br>

2.คลิกที่ "เพิ่มเติม" จะแสดงเมนูเพิ่มเติม จากนั้นคลิกที่ "จัดการสินค้า" เพื่อไปยังหน้าจัดการสินค้า<br><br>
<img src="/images/manual/adding-shipping-method/my-shop-overview-btn.png">

<br><br>

3.คลิกที่ "วิธีการจัดส่งสินค้า" เพื่อไปยังหน้าวิธีการจัดส่งสินค้า<br><br>
<img src="/images/manual/adding-shipping-method/shipping-method.png">

<br><br>

4.คลิกที่ "เพิ่มวิธีการจัดส่งสินค้า"<br><br>
<img src="/images/manual/adding-shipping-method/shipping-method-adding-btn.png">

<br><br>

5.กรอกวิธีการจัดส่งที่ต้องการ<br><br>
<img src="/images/manual/adding-shipping-method/shipping-method-adding-1.png">

<br><br>

6.เมื่อกรอกข้อมูลเสร็จแล้ว ให้คลิกที่ "เพิ่มวิธีการจัดส่งสินค้า" เพื่อบันทึกวิธีการจัดส่ง<br><br>
<img src="/images/manual/adding-shipping-method/shipping-method-adding-2.png">

<br><br>

7.วิธีการจัดส่งสินค้าถูกบันทึกแล้ว สามารถแก้ไขหรือลบได้หากต้องการ<br><br>
<img src="/images/manual/adding-shipping-method/shipping-method-adding-3.png">

<br><br>

8.วิธีการจัดส่งสินค้าจะเป็นตัวเลือกเพื่อให้ลูกค้าเลือกในหน้าสั่งซื้อสินค้า<br><br>
<img src="/images/manual/adding-shipping-method/shipping-method-option.png">