การแจ้งการชำระเงินจะเกิดขึ้นต่อเมื่อรายการสั่งซื้อนั้นได้ถูกยืนยันจากผู้ขาย และหลังจากนั้นจะแสดงจำนวนเงินที่ต้องชำระไปยังผู้ซื้อเพื่อให้ผู้ซื้อสามารถขำระเงินได้ถูกต้องตามที่ผู้ขายระบุไว้
<br><br>

<strong>ขั้นตอนการแจ้งการชำระเงิน</strong><br>

1.เมื่อผู้ขายได้ยืนยันการชำระเงินแล้วจะมีการแจ้งเตือนมายังผู้ซื้อ คลิกรูประฆัง เมื่อคลิกแล้วจะแสดงการแจ้งเตือน<br><br>
<img src="/images/manual/payment-inform/top-bar.png">
<br><br>
<img src="/images/manual/payment-inform/payment-inform-1.png">

<br><br>

2.คลิกที่ "ข้อความการแจ้งเตือน" เพื่อแสดงรายละเอียดการชำระเงิน<br><br>
<img src="/images/manual/payment-inform/payment-inform-2.png">

<br><br>

2.จะแสดงจำนวนเงินที่ต้องชำระของการสั่งซื้อ<br><br>
<img src="/images/manual/payment-inform/payment-inform-3.png">

<br><br>

3.เมื่อชำระเงินแล้วและต้องการแจ้งการชำระเงิน สามารถแจ้งการชำระเงินได้ทันทีโดยคลิกที่ "แจ้งการชำระเงิน" หรือ "แจ้งชำระเงินด้วยวีธีนี้"<br><br>
<img src="/images/manual/payment-inform/payment-inform-4.png">

<br><br>

4.กรอกรายละเอียดของการชำระเงินตามที่ระบุให้ครบถ้วน จากนั้นคลิกที่ "แจ้งการชำระเงิน" เพื่อบันทึกและแจ้งไปยังผู้ขาย<br><br>
<img src="/images/manual/payment-inform/payment-inform-5.png">
<br><br>
<img src="/images/manual/payment-inform/payment-inform-6.png">

<br><br>

5.เมื่อแจ้งการชำระเงินแล้วข้อมูลการแจ้งการชำนะเงินจะถูกส่งไปยังผู้ขาย และเมื่อผู้ขายได้ยืนยันการชำระเงินแล้วจะมีข้อความแจ้งเตือนกลับมายังผู้ซื้อเพื่อบอกให้ผู้ซื้อทราบว่ารายการสั่งซื้อได้ชำระเงินแล้ว<br><br>
<img src="/images/manual/payment-inform/payment-inform-7.png">

<br><br>

6.การแจ้งเตือนหลังจากผู้ขายได้ยืนยันการชำระเงินแล้ว<br><br>
<img src="/images/manual/payment-inform/payment-inform-8.png">