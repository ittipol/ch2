1.เมนูด้านบนคลิกรูปโปรไฟล์ เมื่อคลิกแล้วจะแสดงร้านค้าของคุณ<br><br>
<img src="/images/manual/checking-order/top-bar.png">

<br><br>

2.คลิกที่ "รายการสั่งซื้อสินค้า" เพื่อไปยังหน้าประวัติการสั่งซื้อ<br><br>
<img src="/images/manual/checking-order/my-order.png">

<br><br>

3.จะแสดงหน้ารายการการสั่งซื้อพร้อมรายละเอียดการสักซื้อ<br><br>
<img src="/images/manual/checking-order/checking-order-1.png">

<br><br>

4.เมื่อต้องการดูรายละเอียดของการสั่งซื้อให้คลิกที่ "เลขที่การสั่งซื้อ" เพื่อไปยังหน้ารายละเอียดการสั่งซื้อ<br><br>
<img src="/images/manual/checking-order/checking-order-2.png">

<br><br>

5.ในหน้ารายละเอียดการสั่งซื้อจะแสดงข้อมูลของการสั่งซื้อ เช่น สถานะการสั่งซื้อ รายการสินค้า ประวัติการสั่งซื้อ และรายละเอียดอื่นๆที่เกี่ยวกับการสั่งซื้อ<br><br>
<img src="/images/manual/checking-order/checking-order-3.png">
