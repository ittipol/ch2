สามารถแก้ไขรูปโปรไฟล์ ซื่อ ที่อยู่ และอื่นได้จากในหน้าแก้ไขโปรไฟล์
<br><br>

<strong>แก้ไขโปรไฟล์</strong><br>
1.เมื่อต้องการแก้ไขโปรไฟล์ เมนูด้านบนคลิกรูปโปรไฟล์<br><br>
<img src="/images/manual/profile-edit/top-bar.png">

<br><br>

2.จะแสดงรายละเอียดบัญชีของคุณ จากนั้นคลิกที่ "แก้ไขโปรไฟล์" เพื่อไปยังหน้าแก้ไขโปรไฟล์<br><br>
<img src="/images/manual/profile-edit/account-display.png">

<br><br>

3.หน้าแก้ไขโปรไฟล์ สามารถแก้ไขโปรไฟล์ของคุณได้จากในหน้านี้<br><br>
<img src="/images/manual/profile-edit/profile-edit-1.png">

<br><br>

4.เมื่อแก้ไขเสร็จสิ้นแล้ว ให้คลิกที่ "บันทึก" เพื่อบันทึกการแก้ไข<br><br>
<img src="/images/manual/profile-edit/profile-edit-2.png">